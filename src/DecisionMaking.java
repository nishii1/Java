public class DecisionMaking {
    public static void main(String[] args)
    {
        int num = -9;
        if (num==0)
        {
            System.out.println("Zero");
        }
        else if (num>0)
        {
            System.out.println("Positive");
        }
        {
            System.out.println("Negative");
        }
        System.out.println("End");
    }
}